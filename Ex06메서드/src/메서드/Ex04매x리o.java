package 메서드;

public class Ex04매x리o {

	public static void main(String[] args) {
		//매개변수는 없는데 리턴타입이 있다.
		
		getName();
		System.out.println(getName());
		//결과값으로 내 이름이 보일수 있도록
	
		
	}
	
	//getName() 만들기 : 매개변수 x , 리턴타입 o
	public static String getName() {
		
		//리턴타입에 자료형이 있으면 return 이라는 키워드가 따라온다.
		return "정다운";
		
	}

}
